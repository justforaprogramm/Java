
public class AditonBisMultiplikation {

	public static void main(String[] args) {
		int Number1 = 3 + 4;
		System.out.println(Number1);
		
		char Num = 0;
		System.out.println(Num);
		
		Number1 = 3 - 4;
		System.out.println(Number1);
		
		Number1 = 3 * 4;
		System.out.println(Number1);
		
		Number1 = 3 / 4;
		System.out.println(Number1);
		
		Number1 = 2 / 1;
		System.out.println(Number1);
		
		System.out.println(Num);
		System.out.println(Num);
		
		int N1 = 3;
		int N2 = 5;
		int result = N1 * N2;
		System.out.println(result);
		
		int Nr1 = 4;
		int Nr2 = 5;
		System.out.println(Nr1 + "" + Nr2);
		System.out.println(Nr1 + Nr2);
	}

}
