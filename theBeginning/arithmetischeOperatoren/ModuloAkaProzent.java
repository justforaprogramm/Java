
public class ModuloAkaProzent {

	public static void main(String[] args) {
		
		char clear = 0;
		
		System.out.println("32 / 3");
		
		int N1 = 32 / 3;
		int N2 = 32 % 3;
		System.out.println(N1 + "," + N2 + "   weil 2 übrig bleibt");
		
		System.out.println(clear);
		
		String t1 = "31 / 3";
		System.out.println(t1);
		
		int N3 = 31 / 3;
		int N4 = 31 % 3;
		System.out.println(N3 + "," + N4 + "   weil 1 übrig bleibt");
		
		System.out.println(clear);
		
		System.out.println("30 / 3");
		
		int N5 = 30 / 3;
		int N6 = 30 % 3;
		System.out.println(N5 + "," + N6 + "   weil 0 übrig bleibt");
	}

}
